public abstract class Ident {
	private String type;

	/**
	 * 
	 * @return
	 */
	public String getType() {
		return type;
	}

	/**
	 * 
	 * @param type
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * 
	 * @return
	 */
	public abstract boolean isVar();
	
	@Override
	/**
	 * 
	 */
	public String toString() {
		return "Ident [type=" + type + "]";
	}



}
